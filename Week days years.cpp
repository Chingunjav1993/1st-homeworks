#include<iostream>
using namespace std;
int main(){
	
	
	//day to  weeks
	double q;
	double week;
	cout<<"Please Enter Days ";
	cin>>q;
	week = q/7;
	cout<<q<<" "<<"days is "<<week<<" "<<"weeks "<<endl;
	
	
	//day to years
	double w;
	double year;
	cout<<"Please Enter Days ";
	cin>>w;
	year = w/365;
	cout<<w<<" "<<"days is "<<year<<" "<<"years";
	
	
	
	
	
	
	return 0;
	
}
